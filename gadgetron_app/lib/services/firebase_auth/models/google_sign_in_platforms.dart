/// An enumeration of platforms supported by Google Sign-In.
///
/// This enumeration is used to specify the platforms supported by Google Sign-In via the (google_sign_in plugin)[https://pub.dev/packages/google_sign_in].
enum GoogleSignInPlatforms {
  /// The Android platform.
  android,

  /// The iOS platform.
  iOS,

  /// The macOS platform.
  macOS,

  /// The web platform.
  web;
}
